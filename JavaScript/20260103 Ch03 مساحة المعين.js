
function area_of_rhombus(q, p) {
    return (q * p) / 2;
}


///// TEST 1 /////
// Inputs
q = 6
p = 7

// Outputs
console.log(area_of_rhombus(q, p)); // 21


///// TEST 2 /////
// Inputs
q = 3
p = 6.6

// Outputs
console.log(area_of_rhombus(q, p)); // 9.9


///// TEST 3 /////
// Inputs
