
function num_elements(my_array) {
    return my_array.length;
}


///// TEST 1 /////
// Inputs
my_array = [5, 12, 3, 51]

// Outputs
console.log(num_elements(my_array)); // 4


///// TEST 2 /////
// Inputs
my_array = [19, 12]

// Outputs
console.log(num_elements(my_array)); // 2


///// TEST 3 /////
// Inputs
my_array = [10, 24, 45, 123, 124, 982, 51, 42, 124, 19]

// Outputs
console.log(num_elements(my_array)); // 10


